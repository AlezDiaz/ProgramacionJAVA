    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try {         
            ProductoDAO productoDAO = new ProductoDAO();
            ProductoEntidad param = new ProductoEntidad();
            param.setNombreProducto(this.txtNombre.getText());
            param.setPrecioProducto(Double.parseDouble(this.txtPrecio.getText()));
            param.setStockProducto(Integer.parseInt(this.txtStock.getText()));
            productoDAO.AgregarProducto(param);
            
            JOptionPane.showMessageDialog(null, 
                    "Agrego un nuevo producto Satisfactoriamente",
                    "IDAT Informacion", JOptionPane.INFORMATION_MESSAGE);
            
            int ConfirmarOtroRegistro = JOptionPane.showConfirmDialog(null, 
                    "Desea agregar un nuevo Producto?", "Confirmacion", 
                    JOptionPane.OK_CANCEL_OPTION);            

            if (ConfirmarOtroRegistro == JOptionPane.OK_OPTION) {
                txtNombre.setText("");
                txtPrecio.setText("");
                txtStock.setText("");
            } else {
                new frmBuscarProducto().setVisible(true);
                this.setVisible(false);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, 
                    "Error en el Sistema, valide la informaci√≥n ingresada",
                    "IDAT Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

