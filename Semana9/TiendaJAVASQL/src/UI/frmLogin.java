    private void btnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccederActionPerformed
        // TODO add your handling code here:
        String usuario = txtUsuario.getText();
        String password = txtPassword.getText();
        boolean pudoAcceder = false;
        
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        pudoAcceder = usuarioDAO.AccesoSistema(usuario, password);
        
        if (pudoAcceder == true){
            JOptionPane.showMessageDialog(null, 
                    "Bienvenido al Sistema", "IDAT: Informaci√≥n", 
                    JOptionPane.INFORMATION_MESSAGE);
            
            this.setVisible(false); //Ocultar formulario LOGIN
            new frmBuscarProducto().setVisible(true); //Mostrar formulario BUSCAR PRODUCTO
        } else {
            JOptionPane.showMessageDialog(null, 
                    "Error no coincide usuario y/o password", "IDAT: Errores", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAccederActionPerformed

