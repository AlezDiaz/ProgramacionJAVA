--- AGREGAR PRODUCTO

CREATE PROCEDURE USP_AGREGAR_PRODUCTO
@NOM_PRODUCTO VARCHAR(50),
@STOCK_PRODUCTO INT,
@PRE_PRODUCTO DECIMAL(19,2)
AS
BEGIN
INSERT INTO TA_PRODUCTO
(
NOM_PRODUCTO,
STOCK_PRODUCTO,
PRE_PRODUCTO
) VALUES
(
@NOM_PRODUCTO,
@STOCK_PRODUCTO,
@PRE_PRODUCTO
)
END

SP_HELP TA_PRODUCTO
